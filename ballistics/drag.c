/**
 * @文件 drag.c
 * @简介 用于炮弹阻力系数的分段线性Cd(马赫数)插值。
 *
 * 提供作为马赫数函数的阻力系数查找表。
 * 该表模拟了跨声速阻力上升（Cd在M=1.0附近达到峰值1.15），
 * 随后是超声速衰减。这些数值是针对钝头
 * 炮弹（42cm大贝莎炮弹）基于G1/G7标准阻力
 * 模型形状的近似值。
 *
 * Cd曲线形状：
 *   亚声速（M < 0.7）:       Cd ≈ 0.65-0.70（近似恒定）
 *   跨声速（0.7 < M < 1.2）: Cd上升至M=1.0时的1.15，然后下降
 *   超声速（M > 1.2）:       Cd随马赫数单调递减
 *
 * 插值方法：查表断点之间的分段线性插值。
 * 超出查表范围时，使用最近端点的值
 * （恒定外推）。
 */

#include "drag.h"

/**
 * @简介 Cd(马赫数)断点表：{马赫数, Cd值}。
 *
 * 每一行定义了分段线性曲线中的一个节点。表格
 * 按马赫数递增排列。第0列 = 马赫数，第1列 = Cd。
 */
static const float cd_table[][2] = {
    {0.3f, 0.65f},  /**< 低亚声速（M=0.3）：Cd=0.65 */
    {0.7f, 0.70f},  /**< 高亚声速（M=0.7）：Cd=0.70 */
    {0.9f, 0.90f},  /**< 跨声速起始（M=0.9）：Cd=0.90 */
    {1.0f, 1.15f},  /**< 声速峰值（M=1.0）：Cd=1.15 -- 最大阻力 */
    {1.2f, 0.88f},  /**< 低超声速（M=1.2）：Cd降至0.88 */
    {1.5f, 0.62f},  /**< 中超声速（M=1.5）：Cd=0.62 */
    {2.0f, 0.46f},  /**< 高超声速（M=2.0）：Cd=0.46 */
    {2.5f, 0.38f}   /**< 高超音速（M=2.5）：Cd=0.38 */
};
#define CD_TABLE_SIZE 8  /**< Cd查找表中的条目数量 */

/**
 * @简介 查找给定马赫数对应的阻力系数。
 *
 * 在查表断点之间执行分段线性插值。
 * 算法：
 *   - 若马赫数 <= 表中最低值，返回第一个Cd（恒定外推）。
 *   - 否则，找到包含mach的区间[M_i-1, M_i]，计算
 *     线性插值参数t（范围[0,1]），并返回
 *     Cd = Cd_i-1 + t * (Cd_i - Cd_i-1)。
 *   - 若马赫数 > 表中最高值，返回最后一个Cd条目。
 *
 * @参数 mach 马赫数（弹丸速度与当地声速之比）。
 * @返回 阻力系数Cd（无量纲）。
 */
float drag_cd(float mach) {
    if (mach <= cd_table[0][0]) return cd_table[0][1]; /**< 低于查表范围：使用第一个Cd */
    for (int i = 1; i < CD_TABLE_SIZE; i++) {
        if (mach <= cd_table[i][0]) {
            float t = (mach - cd_table[i-1][0]) / (cd_table[i][0] - cd_table[i-1][0]); /**< 插值比例 [0,1] */
            return cd_table[i-1][1] + t * (cd_table[i][1] - cd_table[i-1][1]); /**< 线性混合：Cd1 + t*(Cd2-Cd1) */
        }
    }
    return cd_table[CD_TABLE_SIZE - 1][1]; /**< 超出查表范围：使用最后一个Cd */
}
